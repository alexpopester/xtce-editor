#[cfg(test)]
mod tests {
    use super::*;
    use crate::layout::{DiscriminatorInfo, FieldLayout, LeafContainer, TypeInfo};

    // ── Helpers ───────────────────────────────────────────────────────────────

    fn make_leaf(
        name: &str,
        discriminator: Option<DiscriminatorInfo>,
        fields: Vec<FieldLayout>,
    ) -> LeafContainer {
        let total_bits = fields
            .iter()
            .map(|f| f.bit_offset + f.type_info.size_in_bits())
            .max()
            .unwrap_or(0);
        LeafContainer {
            name: name.to_string(),
            full_path: format!("Root/{name}"),
            discriminator,
            fields,
            total_bits,
        }
    }

    fn make_disc(param_name: &str, value: i64) -> DiscriminatorInfo {
        DiscriminatorInfo { param_name: param_name.to_string(), value }
    }

    fn uint_field(name: &str, bit_offset: u32, size: u32, lsb: bool) -> FieldLayout {
        FieldLayout {
            name: name.to_string(),
            type_info: TypeInfo::Integer {
                signed: false,
                size_in_bits: size,
                byte_order_lsb: lsb,
            },
            bit_offset,
        }
    }

    fn unaligned_field(name: &str, bit_offset: u32, size: u32, lsb: bool) -> FieldLayout {
        // size chosen so (bit_offset % 8 != 0) || (size % 8 != 0) holds
        FieldLayout {
            name: name.to_string(),
            type_info: TypeInfo::Integer {
                signed: false,
                size_in_bits: size,
                byte_order_lsb: lsb,
            },
            bit_offset,
        }
    }

    // ── Task 3: discriminator dispatch ────────────────────────────────────────

    /// All leaves share a discriminator → XTCE_MAP emitted, no unconditional calls.
    #[test]
    fn test_all_discriminated_emits_map_only() {
        let disc = uint_field("APID", 0, 16, false);
        let leaves = vec![
            make_leaf("PktA", Some(make_disc("APID", 100)), vec![disc.clone()]),
            make_leaf("PktB", Some(make_disc("APID", 200)), vec![disc.clone()]),
        ];
        let lua = generate_lua(&leaves, 4321);

        assert!(lua.contains("XTCE_MAP"), "dispatch map should be present");
        assert!(lua.contains("[100] = dissect_pkta"), "APID 100 → PktA");
        assert!(lua.contains("[200] = dissect_pktb"), "APID 200 → PktB");
        assert!(!lua.contains("Undiscriminated"),
            "no undiscriminated section when all leaves have a discriminator");
    }

    /// Mixed leaves: one discriminated, one not → XTCE_MAP + unconditional call.
    #[test]
    fn test_mixed_leaves_emits_map_and_unconditional() {
        let disc = uint_field("APID", 0, 16, false);
        let leaves = vec![
            make_leaf("PktA", Some(make_disc("APID", 100)), vec![disc.clone()]),
            make_leaf("Common", None, vec![uint_field("Status", 0, 8, false)]),
        ];
        let lua = generate_lua(&leaves, 4321);

        assert!(lua.contains("XTCE_MAP"), "dispatch map should be present");
        assert!(lua.contains("[100] = dissect_pkta"), "APID 100 → PktA");
        assert!(lua.contains("Undiscriminated"),
            "undiscriminated section should be present");
        assert!(lua.contains("dissect_common(buf, pkt, subtree)"),
            "Common should be called unconditionally");
        // Common must NOT be in the XTCE_MAP
        assert!(!lua.contains("dissect_common,"),
            "Common must not appear in XTCE_MAP");
    }

    /// No discriminators at all → fallback path calls every dissect function.
    #[test]
    fn test_no_discriminator_calls_all() {
        let leaves = vec![
            make_leaf("PktA", None, vec![uint_field("A", 0, 8, false)]),
            make_leaf("PktB", None, vec![uint_field("B", 0, 8, false)]),
        ];
        let lua = generate_lua(&leaves, 4321);

        assert!(!lua.contains("XTCE_MAP"), "no dispatch map without discriminators");
        assert!(lua.contains("dissect_pkta(buf, pkt, subtree)"));
        assert!(lua.contains("dissect_pktb(buf, pkt, subtree)"));
    }

    // ── Task 4: byte order and alignment ──────────────────────────────────────

    /// Byte-aligned little-endian field → tree:add_le.
    #[test]
    fn test_le_field_uses_add_le() {
        let leaves = vec![make_leaf("Pkt", None, vec![uint_field("Val", 0, 16, true)])];
        let lua = generate_lua(&leaves, 4321);
        assert!(lua.contains("tree:add_le(F[\"pkt.val\"]"),
            "LE byte-aligned field must use add_le");
    }

    /// Byte-aligned big-endian field → tree:add (not add_le).
    #[test]
    fn test_be_field_uses_add() {
        let leaves = vec![make_leaf("Pkt", None, vec![uint_field("Val", 0, 16, false)])];
        let lua = generate_lua(&leaves, 4321);
        assert!(lua.contains("tree:add(F[\"pkt.val\"]"),
            "BE byte-aligned field must use add");
        assert!(!lua.contains("add_le"),
            "BE field must not use add_le");
    }

    /// Non-byte-aligned field → buf():bitfield().
    #[test]
    fn test_unaligned_field_uses_bitfield() {
        // bit_offset=5, size=3 → neither byte-aligned nor byte-sized
        let leaves = vec![make_leaf("Pkt", None, vec![unaligned_field("Val", 5, 3, false)])];
        let lua = generate_lua(&leaves, 4321);
        assert!(lua.contains(":bitfield("),
            "unaligned field must use bitfield");
    }

    /// Unaligned little-endian field → WARNING comment + bitfield fallback.
    #[test]
    fn test_unaligned_le_field_emits_warning_and_bitfield() {
        let leaves = vec![make_leaf("Pkt", None, vec![unaligned_field("Val", 5, 3, true)])];
        let lua = generate_lua(&leaves, 4321);
        assert!(lua.contains("WARNING"),
            "unaligned LE field must emit a WARNING comment");
        assert!(lua.contains(":bitfield("),
            "unaligned LE field must still use bitfield");
        assert!(!lua.contains("add_le"),
            "unaligned LE field must NOT use add_le (not supported by Wireshark)");
    }

    /// LE discriminator → le_uint() read in main dissector.
    #[test]
    fn test_le_discriminator_uses_le_uint() {
        let disc = uint_field("APID", 0, 16, true); // LE
        let leaves = vec![
            make_leaf("PktA", Some(make_disc("APID", 100)), vec![disc.clone()]),
            make_leaf("PktB", Some(make_disc("APID", 200)), vec![disc.clone()]),
        ];
        let lua = generate_lua(&leaves, 4321);
        assert!(lua.contains(":le_uint()"),
            "LE discriminator field must use le_uint()");
        assert!(!lua.contains(":uint()"),
            "LE discriminator must not use uint()");
    }

    /// Bit-unaligned discriminator → bitfield() read in main dissector.
    #[test]
    fn test_unaligned_discriminator_uses_bitfield() {
        // bit_offset=5, size=3 — not byte-aligned
        let disc = unaligned_field("TypeId", 5, 3, false);
        let leaves = vec![
            make_leaf("PktA", Some(make_disc("TypeId", 1)), vec![disc.clone()]),
            make_leaf("PktB", Some(make_disc("TypeId", 2)), vec![disc.clone()]),
        ];
        let lua = generate_lua(&leaves, 4321);
        assert!(lua.contains(":bitfield("),
            "unaligned discriminator must use bitfield()");
        assert!(!lua.contains(":uint()"),
            "unaligned discriminator must not use uint()");
    }
}

/// Find the bit offset, bit size, and byte-order of `param_name` in the first
/// leaf that contains it.  Returns `(bit_offset, size_in_bits, is_lsb)`.
/// Falls back to `(0, 16, false)` if the field is not found.
fn find_field_location(leaves: &[LeafContainer], param_name: &str) -> (u32, u32, bool) {
    for lc in leaves {
        if let Some(f) = lc.fields.iter().find(|f| f.name == param_name) {
            let is_lsb = matches!(
                &f.type_info,
                TypeInfo::Integer { byte_order_lsb: true, .. }
                    | TypeInfo::Float { byte_order_lsb: true, .. }
            );
            return (f.bit_offset, f.type_info.size_in_bits(), is_lsb);
        }
    }
    (0, 16, false)
}
