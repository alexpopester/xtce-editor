/// Generate a complete Wireshark Lua dissector for the given leaf containers.
///
/// The dissector listens on UDP `port`.  If all containers share a common
/// single-parameter equality discriminator (e.g. an APID field), the
/// dissector dispatches by that value.  Otherwise it shows all fields of all
/// containers without dispatch.
pub fn generate_lua(leaves: &[LeafContainer], port: u16) -> String {
    let mut out = String::with_capacity(4096);

    out.push_str("-- Auto-generated XTCE Wireshark Lua dissector\n");
    out.push_str("-- Generated by xtce-tools gen-dissector\n\n");

    out.push_str("local xtce_proto = Proto(\"xtce\", \"XTCE Telemetry\")\n\n");

    // ── ProtoField declarations ─────────────────────────────────────────────
    out.push_str("-- ProtoField declarations\n");
    out.push_str("local F = {}\n");
    for lc in leaves {
        let prefix = lua_id(&lc.name);
        for field in &lc.fields {
            if field.name.starts_with("_pad_") {
                continue;
            }
            let key = format!("{}.{}", prefix, lua_id(&field.name));
            let abbrev = format!("xtce.{}.{}", prefix, lua_id(&field.name));
            let label = &field.name;
            let proto_field = proto_field_call(&abbrev, label, &field.type_info);
            out.push_str(&format!("F[\"{key}\"] = {proto_field}\n"));
        }
    }
    out.push('\n');

    // Register all fields with the proto.
    out.push_str("local _all_fields = {}\n");
    out.push_str("for _, f in pairs(F) do _all_fields[#_all_fields+1] = f end\n");
    out.push_str("xtce_proto.fields = _all_fields\n\n");

    // ── Per-container dissect functions ────────────────────────────────────
    for lc in leaves {
        emit_container_function(&mut out, lc);
    }

    // ── Discriminator map (if applicable) ──────────────────────────────────
    let discriminator_param = detect_common_discriminator(leaves);

    if let Some(param_name) = &discriminator_param {
        out.push_str("-- Discriminator dispatch map\n");
        out.push_str("local XTCE_MAP = {\n");
        for lc in leaves {
            if let Some(d) = &lc.discriminator {
                out.push_str(&format!(
                    "    [{}] = dissect_{},\n",
                    d.value,
                    lua_id(&lc.name)
                ));
            }
        }
        out.push_str("}\n\n");

        // Determine bit offset and size of the discriminator field in a base
        // container that is common to all leaves (best effort: use the first
        // leaf's field list to locate the parameter).
        let (disc_bit_offset, disc_size_bits, disc_lsb) =
            find_field_location(leaves, param_name);
        let min_len = (disc_bit_offset + disc_size_bits + 7) / 8;

        out.push_str("-- Main dissect function\n");
        out.push_str("function xtce_proto.dissector(buf, pkt, tree)\n");
        out.push_str("    pkt.cols.protocol = \"XTCE\"\n");
        out.push_str("    local subtree = tree:add(xtce_proto, buf(), \"XTCE Telemetry\")\n");
        out.push_str(&format!("    if buf:len() < {min_len} then return end\n"));

        // Emit the discriminator read, handling byte-aligned vs bit-packed.
        if disc_bit_offset % 8 == 0 && disc_size_bits % 8 == 0 {
            let byte_off = disc_bit_offset / 8;
            let byte_len = disc_size_bits / 8;
            let uint_fn = if disc_lsb { "le_uint" } else { "uint" };
            out.push_str(&format!(
                "    local disc_val = buf({byte_off}, {byte_len}):{uint_fn}()\n"
            ));
        } else {
            // Unaligned discriminator — use bitfield() (always big-endian bit order).
            let byte_off = disc_bit_offset / 8;
            let bit_in_byte = disc_bit_offset % 8;
            let byte_span = (bit_in_byte + disc_size_bits + 7) / 8;
            out.push_str(&format!(
                "    local disc_val = buf({byte_off}, {byte_span}):bitfield({bit_in_byte}, {disc_size_bits})\n"
            ));
        }

        out.push_str("    local fn = XTCE_MAP[disc_val]\n");
        out.push_str("    if fn then fn(buf, pkt, subtree)\n");
        out.push_str("    else subtree:add(\"Unknown packet type: \" .. disc_val) end\n");

        // Containers without a discriminator are always decoded (e.g. shared headers).
        let undiscriminated: Vec<_> =
            leaves.iter().filter(|l| l.discriminator.is_none()).collect();
        if !undiscriminated.is_empty() {
            out.push_str("    -- Undiscriminated containers (always decoded)\n");
            for lc in &undiscriminated {
                out.push_str(&format!(
                    "    dissect_{}(buf, pkt, subtree)\n",
                    lua_id(&lc.name)
                ));
            }
        }

        out.push_str("end\n\n");
    } else {
        // No discriminator — just show the first container's fields (or all).
        out.push_str("-- No common discriminator found; showing all containers\n");
        out.push_str("function xtce_proto.dissector(buf, pkt, tree)\n");
        out.push_str("    pkt.cols.protocol = \"XTCE\"\n");
        out.push_str("    local subtree = tree:add(xtce_proto, buf(), \"XTCE Telemetry\")\n");
        for lc in leaves {
            out.push_str(&format!(
                "    dissect_{}(buf, pkt, subtree)\n",
                lua_id(&lc.name)
            ));
        }
        out.push_str("end\n\n");
    }

    // ── UDP registration ────────────────────────────────────────────────────
    out.push_str(&format!("local PORT = {port}\n"));
    out.push_str("local udp_table = DissectorTable.get(\"udp.port\")\n");
    out.push_str("udp_table:add(PORT, xtce_proto)\n");

    out
}

