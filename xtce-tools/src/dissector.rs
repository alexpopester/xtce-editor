/// Sanitise a name for use as a Lua identifier component.
pub fn lua_id(name: &str) -> String {
    name.chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>()
        .to_lowercase()
}

/// Return the `ProtoField.<type>(...)` call for a given field.
fn proto_field_call(abbrev: &str, label: &str, ti: &TypeInfo) -> String {
    match ti {
        TypeInfo::Integer { signed, size_in_bits, .. } => {
            let (pf_type, base) = match (signed, size_in_bits) {
                (false, 8) => ("uint8", "base.DEC"),
                (false, 16) => ("uint16", "base.DEC"),
                (false, 32) => ("uint32", "base.DEC"),
                (false, 64) => ("uint64", "base.DEC"),
                (true, 8) => ("int8", "base.DEC"),
                (true, 16) => ("int16", "base.DEC"),
                (true, 32) => ("int32", "base.DEC"),
                (true, 64) => ("int64", "base.DEC"),
                (false, _) => ("uint32", "base.DEC"),
                (true, _) => ("int32", "base.DEC"),
            };
            format!("ProtoField.{pf_type}(\"{abbrev}\", \"{label}\", {base})")
        }
        TypeInfo::Float { size_in_bits, .. } => {
            let pf = if *size_in_bits == 64 { "double" } else { "float" };
            format!("ProtoField.{pf}(\"{abbrev}\", \"{label}\")")
        }
        TypeInfo::Enum { size_in_bits, values } => {
            let table = build_enum_table(values);
            let pf = if *size_in_bits <= 8 {
                "uint8"
            } else if *size_in_bits <= 16 {
                "uint16"
            } else {
                "uint32"
            };
            format!("ProtoField.{pf}(\"{abbrev}\", \"{label}\", base.DEC, {table})")
        }
        TypeInfo::Boolean { .. } => {
            format!("ProtoField.bool(\"{abbrev}\", \"{label}\")")
        }
        TypeInfo::StringField { .. } => {
            format!("ProtoField.string(\"{abbrev}\", \"{label}\")")
        }
        TypeInfo::Binary { .. } | TypeInfo::Unknown { .. } => {
            format!("ProtoField.bytes(\"{abbrev}\", \"{label}\")")
        }
    }
}

fn build_enum_table(values: &[ValueEnumeration]) -> String {
    let mut t = String::from("{");
    for (i, v) in values.iter().enumerate() {
        if i > 0 {
            t.push_str(", ");
        }
        let label = v.label.replace('"', "'");
        t.push_str(&format!("[{}]=\"{}\"", v.value, label));
    }
    t.push('}');
    t
}

/// Emit a `local function dissect_<name>(buf, pkt, tree) ... end` block.
fn emit_container_function(out: &mut String, lc: &LeafContainer) {
    let fn_name = lua_id(&lc.name);
    out.push_str(&format!(
        "-- Container: {} ({})\n",
        lc.name, lc.full_path
    ));
    out.push_str(&format!(
        "local function dissect_{fn_name}(buf, pkt, tree)\n"
    ));

    for field in &lc.fields {
        if field.name.starts_with("_pad_") {
            continue;
        }
        emit_field_add(out, &fn_name, field);
    }

    out.push_str("end\n\n");
}

/// Return true if a `TypeInfo` is little-endian (LSB-first byte order).
fn is_lsb(ti: &TypeInfo) -> bool {
    matches!(
        ti,
        TypeInfo::Integer { byte_order_lsb: true, .. }
            | TypeInfo::Float { byte_order_lsb: true, .. }
    )
}

/// Emit one `tree:add(...)` line for a field.
///
/// Byte-aligned fields use `tree:add` (big-endian) or `tree:add_le`
/// (little-endian).  Unaligned fields always use `buf():bitfield()`, which
/// reads bits in big-endian order — little-endian bit-packed fields are not
/// supported by Wireshark's Lua API and are emitted as big-endian with a
/// comment warning.
fn emit_field_add(out: &mut String, container_id: &str, field: &FieldLayout) {
    let key = format!("{}.{}", container_id, lua_id(&field.name));
    let size = field.type_info.size_in_bits();
    let bit_off = field.bit_offset;
    let lsb = is_lsb(&field.type_info);

    if bit_off % 8 == 0 && size % 8 == 0 {
        // Byte-aligned — simple slice with correct endian method.
        let byte_off = bit_off / 8;
        let byte_len = size / 8;
        let add_fn = if lsb { "add_le" } else { "add" };
        out.push_str(&format!(
            "    if buf:len() >= {} then tree:{add_fn}(F[\"{key}\"], buf({byte_off}, {byte_len})) end\n",
            byte_off + byte_len
        ));
    } else {
        // Sub-byte or unaligned — use bitfield() (big-endian bit order only).
        let byte_off = bit_off / 8;
        let bit_in_byte = bit_off % 8;
        let byte_span = ((bit_in_byte + size) + 7) / 8;
        if lsb {
            out.push_str(&format!(
                "    -- WARNING: {key} is little-endian but unaligned; decoded as big-endian\n"
            ));
        }
        out.push_str(&format!(
            "    if buf:len() >= {} then tree:add(F[\"{key}\"], buf({byte_off}, {byte_span}):bitfield({bit_in_byte}, {size})) end\n",
            byte_off + byte_span
        ));
    }
}

/// If all leaf containers with discriminators share the same parameter name,
/// return that name.
fn detect_common_discriminator(leaves: &[LeafContainer]) -> Option<String> {
    let discriminated: Vec<_> = leaves.iter().filter(|l| l.discriminator.is_some()).collect();
    if discriminated.is_empty() {
        return None;
    }
    let first = discriminated[0].discriminator.as_ref().unwrap().param_name.clone();
    if discriminated
        .iter()
        .all(|l| l.discriminator.as_ref().unwrap().param_name == first)
    {
        Some(first)
    } else {
        None
    }
}

